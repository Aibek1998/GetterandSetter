public class Course {
    private String course;
    private String name;
    private String number;
    private String fristlesson;

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getFristlesson() {
        return fristlesson;
    }

    public void setFristlesson(String fristlesson) {
        this.fristlesson = fristlesson;
    }
}
